package com.bw.fit.common.models;

public class Function {

	private String function_name ;
	private int function_id ;
	private String function_level ;
	private String function_url ;
	private String function_target ;
	private String function_rel ;
	private int function_p_id ;
	
	public int getFunction_p_id() {
		return function_p_id;
	}
	public void setFunction_p_id(int function_p_id) {
		this.function_p_id = function_p_id;
	}
	public String getFunction_name() {
		return function_name;
	}
	public void setFunction_name(String function_name) {
		this.function_name = function_name;
	}
	public int getFunction_id() {
		return function_id;
	}
	public void setFunction_id(int function_id) {
		this.function_id = function_id;
	}
	public String getFunction_level() {
		return function_level;
	}
	public void setFunction_level(String function_level) {
		this.function_level = function_level;
	}
	public String getFunction_url() {
		return function_url;
	}
	public void setFunction_url(String function_url) {
		this.function_url = function_url;
	}
	public String getFunction_target() {
		return function_target;
	}
	public void setFunction_target(String function_target) {
		this.function_target = function_target;
	}
	public String getFunction_rel() {
		return function_rel;
	}
	public void setFunction_rel(String function_rel) {
		this.function_rel = function_rel;
	}
	
}
